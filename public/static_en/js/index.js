$('.index_menu .event').click(function(){
	$('body').addClass('open');
	let index=$(this).index()+1;
	if(index > 4){
		$('.menupop').show();
	}
	let id=1;
	if(index==5){
	    id=1;
	}
	if(index==6){
	    id=2;
	}
	if(index==7){
	    id=3;
	}
	if(index==8){
	    id=4;
	}
    $.ajax({
                        url:"/index/my/detail.html?id="+id,
                        data:{},
                        type:'POST',
                        success:function(data){
                            if(data.code==1){
                                $('#title_alert').html(data.data.title);
                                $('.menupop .bg .b').html(data.data.content)
                               // $('.menupop .bg').html(indexmenu_08)
                            }else{
                                msgShow(data.info);
                            }
                        }
                    });
	return false;
});

$('.menupop').bind('click','.menupopHide',function(){
	$('body').removeClass('open');
	$('.menupop .bg .b').html('');
	$('.menupop').hide();
})